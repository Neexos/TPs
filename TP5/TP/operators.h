float somme (float num1, float num2){
    return num1+num2;
}

float difference (float num1, float num2){
    return num1-num2;
}

float produit (float num1, float num2){
    return num1*num2;
}

float quotient (float num1, float num2){
    return num1/num2;
}

int modulo (int num1, int num2){
    return num1%num2;
}

int et (int num1, int num2){
    return num1&num2;
}

int ou (int num1, int num2){
    return num1|num2;
}

int negation (int num1){
    return ~num1;
}
